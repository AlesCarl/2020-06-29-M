	public List<Director> listAllDirectorsYears(int anno){
		String sql = "select d.`id`, d.`first_name`, d.`last_name` , count(m.`id`) as cn "
				+ "from movies_directors md, movies m, directors d "
				+ "where m.`id`= md.`movie_id` and m.`year`= ? and "
				+ "d.`id`= md.`director_id` "
				+ "group by d.`id` "
				+ "having cn >=1";
		
		List<Director> result = new ArrayList<Director>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setInt(1, anno);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Director director = new Director(res.getInt("id"), res.getString("first_name"), res.getString("last_name"));
				
				result.add(director);
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}

	public List<Integer> getAttoriRegista(Director a1, int anno) {
		
		String sql = " select r.`actor_id` "
				+ "from movies_directors md, movies m, roles r "
				+ "where m.`id`= md.`movie_id` and m.`year`= ? and "
				+ "m.`id`= r.`movie_id`and md.`director_id`= ? " ;
		
		List<Integer> result = new ArrayList<>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setInt(1, anno);
			st.setInt(2, a1.getId());
			ResultSet res = st.executeQuery();
			
			while (res.next()) {

				Integer intt= res.getInt("r.actor_id"); 
				result.add(intt);
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	
	
	
	
	
	
